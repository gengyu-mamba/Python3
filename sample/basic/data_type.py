# List（列表）
# Tuple（元组）
# Set（集合）
# Dictionary（字典）

# 不可变数据：Number（数字），String（字符串），Tuple（元组）
# 可变数据：List（列表），Set（集合），Dictionary（字典）

a = 1
b = 1.0
c = '1.0'
d = [1, 2]
e = (1, 2)
f = {1, 2}
g = {1:'C/C++', 2:'Java', 3:'Python'}

print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)